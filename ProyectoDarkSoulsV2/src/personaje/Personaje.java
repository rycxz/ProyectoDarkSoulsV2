/**
 * el jugador dara valor a todos los atributos y el atributo vida se le dara un valor segun las clases que tenga.
 * 
 * el estado es un atributo el cual el jugador no tendra acceso y no podra modifcar
 */

package clases;

public class Personaje {
	private String nombre;
	private String sexo;
	private String clase;
	private int vida;
	private int ataque;

	public Personaje(String nombre, String sexo, String clase, int vida, int ataque) {
		this.nombre = nombre;
		this.sexo = sexo;
		this.clase = clase;
		this.vida = vida;
		this.ataque = ataque;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getSexo() {
		return sexo;
	}

	public void setSexo(String sexo) {
		this.sexo = sexo;
	}

	public String getClase() {
		return clase;
	}

	public void setClase(String clase) {
		this.clase = clase;
	}

	public int getVida() {
		return vida;
	}

	public void setVida(int vida) {
		this.vida = vida;
	}

	public int getAtaque() {
		return ataque;
	}

	public void setAtaque(int ataque) {
		this.ataque = ataque;
	}

	@Override
	public String toString() {
		return "Tu personaje se llama: " + nombre + " y es del sexo: " + sexo + ", es de la Clase " + clase
				+ ", tiene " + vida + " puntos de vida" + " , y hace " + ataque + " puntos de ataque ";
	}

}
